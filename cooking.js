  static getCraftableRecipes(inventory) {
    const craftable = {};

    for (const [recipeName, [ingredients, xp, time, building]] of Object.entries(Cooking.recipes)) {
        let maxCraftable = Infinity;
        let canCraft = true;

        for (const [ingredient, requiredAmount] of Object.entries(ingredients)) {
        const available = inventory[ingredient] ?? 0;

        if (available < requiredAmount) {
            canCraft = false;
            break;
        }

        maxCraftable = Math.min(
            maxCraftable,
            Math.floor(available / requiredAmount)
        );
        }

        if (canCraft && maxCraftable > 0) {
        craftable[recipeName] = {
            ingredients,
            xp,
            time,
            building,
            maxCraftable
        };
        }
    }

    return craftable;
    }

